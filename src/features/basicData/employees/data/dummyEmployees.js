// Helper functions for employee data management
export const getEmployeeById = (id) => {
  return dummyEmployees.find(emp => emp.id === id);
};

export const getEmployeesByDepartment = (department) => {
  return dummyEmployees.filter(emp => emp.department === department);
};

export const getActiveEmployees = () => {
  return dummyEmployees.filter(emp => emp.status === 'Active');
};

export const getDepartments = () => {
  return [...new Set(dummyEmployees.map(emp => emp.department))].sort();
};

export const getPositions = () => {
  return [...new Set(dummyEmployees.map(emp => emp.position))].sort();
};

export const getEmployeeStats = () => {
  const total = dummyEmployees.length;
  const active = dummyEmployees.filter(emp => emp.status === 'Active').length;
  const onLeave = dummyEmployees.filter(emp => emp.status === 'On Leave').length;
  const inactive = dummyEmployees.filter(emp => emp.status === 'Inactive').length;
  
  const departmentCounts = dummyEmployees.reduce((acc, emp) => {
    acc[emp.department] = (acc[emp.department] || 0) + 1;
    return acc;
  }, {});

  return {
    total,
    active,
    onLeave,
    inactive,
    departmentCounts
  };
};